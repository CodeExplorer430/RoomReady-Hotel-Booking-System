    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        String input = fullNameField.getText();
        if (isValidFullName(input)){
            // Perform insertion of the full name into the system
            JOptionPane.showMessageDialog(this, "Full name inserted: " + input);
        } else {
           JOptionPane.showMessageDialog(this, "Invalid format. Please enter a valid full name.");
           errorMessageLabel.setText("Invalid format. Please enter a valid full name.");
        } 
    }//GEN-LAST:event_insertButtonActionPerformed

