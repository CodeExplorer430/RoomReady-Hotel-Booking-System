    private void validateInput(){
        String input = fullNameField.getText();
        if(isValidFullName(input)){
            errorMessageLabel.setText("");
        } else {
            errorMessageLabel.setText("Invalid format. Please enter a valid full name.");
        }
    }
    
    private boolean isValidFullName(String input){
        // Regular expression pattern allowing variations in full name format and disallowing single-word names
        Pattern pattern = Pattern.compile("^[A-Za-z]+(?:\\s[A-Za-z]+)+\\.?(\\s[A-Za-z]+\\.?)?(\\s[A-Za-z]+\\.?(?:\\s(Jr\\.|Sr\\.|I|II|III|IV|V|VI|VII|VIII|IX))?)?$");
        return pattern.matcher(input).matches();
    }
    
