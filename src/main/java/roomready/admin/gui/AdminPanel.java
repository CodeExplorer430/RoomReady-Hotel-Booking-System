    private void logoutButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutButtonActionPerformed
        // Display the confirmation dialog
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to logout?", "Logout Confirmation", JOptionPane.YES_NO_OPTION);
        
        // Check the user's choice
        if(option == JOptionPane.YES_OPTION){
        
        // Close the current login menu form
        this.dispose();
        
        // Open the customer login form when the button is clicked
        AdminLogin adminLogin = new AdminLogin(authManager); // Create an instance of the CustomerLogin;
        adminLogin.setVisible(true); // Show the customer login form
        }
    }//GEN-LAST:event_logoutButtonActionPerformed

