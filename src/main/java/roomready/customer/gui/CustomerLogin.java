    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
       String username = usernameField.getText();
       String password = new String(passwordField.getPassword());
       
       // Perform validation
       if (username.isEmpty() || password.isEmpty()){
           //Show error message
           javax.swing.JOptionPane.showMessageDialog(this, "Please enter both username and password.");
           return;
       }
       
       // Perform user authentication
       boolean authenticated = authManager.authenticateUser(username, password);
       
       if (authenticated){
           // Successful login
           // Redirect to the main application or dashboard
           javax.swing.JOptionPane.showMessageDialog(this, "Login Successful!");
           
           // Clear the form fields
           usernameField.setText("");
           passwordField.setText("");
           
           // Retrieves the full name of the admin from the file or from the User object
            String fullName = authManager.getCustomerFullName(username);
            
           // Proceed to the customer dashboard
           CustomerPanel customerPanel = new CustomerPanel(fullName);
           ProceedPayment proceedPayment = new ProceedPayment(username);
           ViewBookings viewBookings = new ViewBookings(username);
           CustomerPayment customerPayment = new CustomerPayment(username);
           customerPanel.setVisible(true);
           
           // Close the login form
           this.dispose();
            
           // Displays the welcome message from the customer dashboard
           javax.swing.JOptionPane.showMessageDialog(this, "Welcome! " + fullName);
           
       } else {
           // Failed login
           // Show error message
           javax.swing.JOptionPane.showMessageDialog(this, "Invalid username or password.");
       }
    }//GEN-LAST:event_loginButtonActionPerformed

